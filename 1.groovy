1. Análise da Distribuição Geográfica dos Discentes:
Mapa de Calor (Heatmap) de Origens Geográficas:
Utilize visualizações como mapas de calor para representar graficamente a concentração de discentes por cidade de origem.
Isso pode revelar áreas de maior e menor concentração de discentes, ajudando a entender a diversidade geográfica do corpo discente.
2. Análise de Distância e Mobilidade:
Análise de Distâncias entre Origens e Destinos:
Calcule distâncias geográficas entre as cidades de origem dos discentes e os campi de destino.
Explore se há correlações entre a distância da cidade de origem ao campus e outras variáveis, como tipo de ingresso, tempo de permanência no curso, entre outros.
3. Análise de Preferências Locais:
Padrões de Escolha de Campus por Localidade:
Agrupe os discentes por região geográfica (por exemplo, por estado ou região) e analise se há padrões consistentes na escolha de determinados campi.
Compare a preferência de campus entre discentes locais (da mesma cidade ou estado do campus) e discentes de outras localidades.
4. Análise Temporal da Origem Geográfica:
Tendências de Origens ao Longo do Tempo:
Analise se há mudanças na distribuição geográfica dos discentes ao longo dos anos.
Identifique se determinadas regiões aumentaram ou diminuíram em representação no corpo discente ao longo do tempo.
5. Análise de Clusters Geográficos:
Clusterização de Localidades:
Utilize técnicas de clusterização para identificar grupos de cidades ou regiões que têm características semelhantes em termos de origem de discentes.
Isso pode ajudar a identificar padrões geográficos mais complexos que não seriam evidentes apenas com análises simples de contagens ou proporções.